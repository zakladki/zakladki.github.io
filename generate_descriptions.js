const missingFile = 'tmp_missing.json';
if (!fs.existsSync(missingFile)) {
  console.error("tmp_missing.json is missing! Please run extract first.");
  process.exit(1);
}

const missingItems = JSON.parse(fs.readFileSync(missingFile, 'utf8'));

console.log(`Processing ${missingItems.length} items from ${missingFile}...`);

let generatedCount = 0;
let manualCount = 0;

missingItems.forEach(item => {
  const url = item.url;
  const text = item.text;
  
  if (manualDictionary[url]) {
    cache[url] = manualDictionary[url];
    manualCount++;
  } else {
    // Check if there is already a manual dict matching the base url (without trail slashes/params)
    let foundBase = false;
    for (const [dictUrl, dictDesc] of Object.entries(manualDictionary)) {
      if (url.startsWith(dictUrl) || dictUrl.startsWith(url)) {
        cache[url] = dictDesc;
        manualCount++;
        foundBase = true;
        break;
      }
    }
    
    if (!foundBase) {
      cache[url] = generateDynamicDescription(url, text);
      generatedCount++;
    }
  }
});

// Save updated cache
fs.writeFileSync(CACHE_FILE, JSON.stringify(cache, null, 2), 'utf8');
console.log(`Finished generation! Total cached in ${CACHE_FILE}: ${Object.keys(cache).length} items.`);
console.log(`- Mapped via Manual Dictionary: ${manualCount}`);
console.log(`- Generated dynamically: ${generatedCount}`);

